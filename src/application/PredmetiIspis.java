package application;

public class PredmetiIspis {
	private String id, naziv, brojStudenata, usmjerenje, semestar, profesori;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNaziv() {
		return naziv;
	}

	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}

	public String getBrStud() {
		return brojStudenata;
	}

	public void setBrStud(String brStud) {
		this.brojStudenata = brStud;
	}

	public String getUsmjerenje() {
		return usmjerenje;
	}

	public void setUsmjerenje(String usmjerenje) {
		this.usmjerenje = usmjerenje;
	}

	public String getSemestar() {
		return semestar;
	}

	public void setSemestar(String semestar) {
		this.semestar = semestar;
	}

	public String getProfesori() {
		return profesori;
	}

	public void setProfesori(String profesori) {
		this.profesori = profesori;
	}

	
}
