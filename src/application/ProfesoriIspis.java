package application;

public class ProfesoriIspis {
	private String id, imePrezime, usmjerenje, predmeti, grupe;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getImePrezime() {
		return imePrezime;
	}

	public void setImePrezime(String imePrezime) {
		this.imePrezime = imePrezime;
	}

	public String getUsmjerenje() {
		return usmjerenje;
	}

	public void setUsmjerenje(String usmjerenje) {
		this.usmjerenje = usmjerenje;
	}

	public String getPredmeti() {
		return predmeti;
	}

	public void setPredmeti(String predmeti) {
		this.predmeti = predmeti;
	}

	public String getGrupe() {
		return grupe;
	}

	public void setGrupe(String grupe) {
		this.grupe = grupe;
	}
	
	
}
